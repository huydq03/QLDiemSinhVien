    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon xoa khong?");
        int selectedRow = tableStudents.getSelectedRow();
        if (confirm == JOptionPane.YES_OPTION && selectedRow != -1) {
            String masv = model.getValueAt(selectedRow, 0).toString();
            Student s = getStudentById(masv);
            if (s != null) {
                listSaving.remove(s);
                model.removeRow(selectedRow + (currentPage - 1) * numsPerPage);
            } else {
                boolean isDeleted = this.deleteStudent(masv);
                if (isDeleted) {
                    model.removeRow(selectedRow);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ban chua chon sinh vien");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

