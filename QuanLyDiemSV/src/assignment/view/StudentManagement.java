    private void loadTable() {
        model.setNumRows(0);
        totalItem = listPaging.size();
        numsPage = (totalItem - 1) / numsPerPage + 1;
        int start = (currentPage - 1) * numsPerPage;
        int end = start + numsPerPage;
        if (end > totalItem) {
            end = totalItem;
        }
        lblPage.setText(currentPage + "/" + numsPage);
        for (Student s : listPaging.subList(start, end)) {
            model.addRow(new Object[]{
                s.getMasv(),
                s.getHoTen(),
                s.getEmail(),
                s.getSoDT(),
                s.getGioiTinh() == 1 ? "Nam" : "Nu",
                s.getDiaChi(),
                s.getHinh()
            });
        }
    }

