    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon sua khong?");
        int selectedRow = tableStudents.getSelectedRow();
        if (confirm == JOptionPane.YES_OPTION && selectedRow != -1) {
            String masv = model.getValueAt(selectedRow, 0).toString();
            int index = getIndexStudent(masv);
            Student studentUpdate = validateStudent();
            if(studentUpdate.getHinh() == null){
                studentUpdate.setHinh((String)model.getValueAt(selectedRow, 6));
            }
            if (index != -1) {
                listSaving.set(index, studentUpdate);
                listPaging.set(selectedRow + (currentPage - 1) * numsPerPage, studentUpdate);
                loadTable();
            } else {
                boolean isUpdate = this.updateStudent(studentUpdate);
                if (isUpdate) {
                    listPaging.set(selectedRow + (currentPage - 1) * numsPerPage, studentUpdate);
                    loadTable();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ban chua chon sinh vien");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

