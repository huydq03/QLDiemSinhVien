    private Student getStudentByFromTable(String masv) {
        for (Student s : listPaging) {
            if (s.getMasv().equals(masv)) {
                return s;
            }
        }
        return null;
    }
    private Student getStudentById(String masv) {
        for (Student s : listSaving) {
            if (s.getMasv().equals(masv)) {
                return s;
            }
        }
        return null;
    }

    private int getIndexStudent(String masv) {
        for (int i = 0; i < listSaving.size(); i++) {
            if (listSaving.get(i).getMasv().endsWith(masv)) {
                return i;
            }
        }
        return -1;
    }
